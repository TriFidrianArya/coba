
public class Elektronik {

	String Merk;
	String Voltase;
	String Berat;

	
	public Elektronik(String Merk, String Voltase, String Berat){
		this.Merk=Merk;
		this.Voltase=Voltase;
		this.Berat=Berat;
	}


	public String getMerk() {
		return Merk;
	}


	public String getVoltase() {
		return Voltase;
	}


	public String getBerat() {
		return Berat;
	}
	
	
}
